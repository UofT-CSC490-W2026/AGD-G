# Subclasses of AttackVLM

class AttackVLMUntargeted(_AttackVLM, UntargetedAttackMethod):
    """
    AttackVLMUntargeted guides the adversarial image away from the clean image
    in an arbitary "direction", only seeking to minimize the semantic similarity
    between the clean and adversarial images.
    """
    def compute_step_loss(self, adv_embed, clean_embed) -> torch.Tensor:
        return self.model(adv_embed, clean_embed)


class AttackVLMImage(_AttackVLM, ImageAttackMethod):
    """
    AttackVLMImage guides the adversarial image towards a target image.
    It can also optionally guide the image away from the original clean image in
    embedding space.
    """
    def __init__(self, model: ImageTargetModel, device: Optional[str] = None):
        super().__init__(model, device)

    def setup(
        self,
        clean_t: torch.Tensor,
        target_image_path: str,
		repel_clean: float = 0.35,
        *args, **kwargs
    ) -> None:
        self.target_embed = self.get_target_features(target_image_path)
        self.repel_clean = repel_clean

    def compute_step_loss(self, adv_embed, clean_embed) -> torch.Tensor:
        sim_target = self.model(adv_embed, self.target_embed)
        sim_clean = self.model(adv_embed, clean_embed)
        step_loss = (-sim_target + self.repel_clean * sim_clean)

    def get_target_features(self, image_path):
        target_pil = Image.open(image_path).convert("RGB")
        target_np = np.array(target_pil.resize((512, 512))).astype(np.float32) / 255.0
        target_t = torch.from_numpy(target_np).permute(2, 0, 1).unsqueeze(0).to(self.device)
        with torch.no_grad():
            return self.model.embed_image(target_t, detach=True)


class AttackVLMText(_AttackVLM, TextAttackMethod):
    """
    AttackVLMText guides the adversarial image towards a text description.
    It can optionally guide the image away from the original clean image in
    embedding space, and also away from an original text description, which
    could be a known caption or a prompt used to generate the clean image.
    """
    def __init__(self, model: TextTargetModel, device: Optional[str] = None):
        super().__init__(model, device)

    def setup(
        self,
        clean_t: torch.Tensor,
        target_text: str,
        source_text: Optional[str] = None,
		repel_clean: float = 0.35,
		repel_source_text: float = 0.5,
        *args, **kwargs
    ) -> None:
        self.target_text_embed = self.model.embed_text(target_text)
        self.repel_clean = repel_clean
        self.repel_source_text = repel_source_text
        if source_text:
            self.source_text_embed = self.model.embed_text(source_text)
        else:
            self.source_text_embed = None

    def compute_step_loss(self, adv_embed, clean_embed) -> torch.Tensor:
        sim_text = self.model(adv_embed, self.target_text_embed)
        sim_clean = self.model(adv_embed, clean_embed)
        step_loss = -sim_text + self.repel_clean * sim_clean
        if self.source_text_embed:
            sim_source_text = self.model(adv_embed, self.source_text_embed)
            step_loss = step_loss + self.repel_source_text * sim_source_text
        return step_loss


class AttackVLMOCR(AttackVLMText, TextAttackMethod):
    """
    AttackVLMOCR is an extension of AttackVLMText which limits perturbations around areas detected as text.
    """
    def setup(
        self,
        clean_t: torch.Tensor,
		ocr_edge_quantile: float = 0.88,
		ocr_std_quantile: float = 0.70,
		ocr_dilate_kernel: int = 7,
		ocr_max_area_ratio: float = 0.22,
        *args, **kwargs
    ) -> None:
        super().setup(*args, **kwargs)
        self.perturbation_mask = self._build_text_like_mask(
            clean_t, edge_quantile=ocr_edge_quantile, std_quantile=ocr_std_quantile, dilate_kernel=ocr_dilate_kernel, max_area_ratio=ocr_max_area_ratio
        )

    #override
    def mask(self, data):
        return data * self.perturbation_mask

    @staticmethod
    def _build_text_like_mask(
        clean_t: torch.Tensor,
        edge_quantile: float,
        std_quantile: float,
        dilate_kernel: int,
        max_area_ratio: float,
    ) -> torch.Tensor:
        gray = 0.299 * clean_t[:, 0:1, :, :] + 0.587 * clean_t[:, 1:2, :, :] + 0.114 * clean_t[:, 2:3, :, :]
        sobel_x = torch.tensor([[[-1, 0, 1], [-2, 0, 2], [-1, 0, 1]]], dtype=gray.dtype, device=gray.device).unsqueeze(1)
        sobel_y = torch.tensor([[[-1, -2, -1], [0, 0, 0], [1, 2, 1]]], dtype=gray.dtype, device=gray.device).unsqueeze(1)
        gx = F.conv2d(gray, sobel_x, padding=1)
        gy = F.conv2d(gray, sobel_y, padding=1)
        grad = torch.sqrt(gx * gx + gy * gy + 1e-12)

        gthr = torch.quantile(grad.flatten(1), edge_quantile, dim=1).view(-1, 1, 1, 1)
        edge_mask = (grad >= gthr).float()

        mean = F.avg_pool2d(gray, kernel_size=9, stride=1, padding=4)
        mean2 = F.avg_pool2d(gray * gray, kernel_size=9, stride=1, padding=4)
        std = torch.sqrt((mean2 - mean * mean).clamp(min=0.0) + 1e-12)
        sthr = torch.quantile(std.flatten(1), std_quantile, dim=1).view(-1, 1, 1, 1)
        std_mask = (std >= sthr).float()

        mask = edge_mask * std_mask
        k = int(max(3, dilate_kernel))
        if k % 2 == 0:
            k += 1
        for _ in range(2):
            mask = F.max_pool2d(mask, kernel_size=k, stride=1, padding=k // 2)

        if mask.mean().item() > max_area_ratio:
            cgrad = grad * mask
            cthr = torch.quantile(cgrad.flatten(1), 1.0 - max_area_ratio, dim=1).view(-1, 1, 1, 1)
            mask = (cgrad >= cthr).float()
            mask = F.max_pool2d(mask, kernel_size=3, stride=1, padding=1)

        return mask.clamp(0, 1)

