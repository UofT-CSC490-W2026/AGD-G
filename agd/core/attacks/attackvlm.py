# AttackVLM base class

class _AttackVLM(ABC):
    """
    This base class for AttackVLM implements the core attack, but leaves some
    important helper methods abstract. The child classes below implement these
    methods in different ways by defining those helpers differently.

    + Every child class must implement a setup method which takes in any extra
      parameters for its attack, pre-computes embeddings if needed, and stores
      results in the class. The purpose of this method is to allow for pre-
      computation of values used in the loss function later.
    + Every child class must implement a step loss function, which the attack
      will try to minimize. This function must be differentiable.
    """
    def __init__(self, model: ImageTargetModel, device: Optional[str] = None):
        self.model = model
        self.device = device or "cuda"

    @abstractmethod
    def compute_step_loss(self, adv_embed, clean_embed) -> torch.Tensor:
        """
        Compute the loss given the embedding for the current adversarial image,
        as generated by the model. Implementing subclasses that need to compare against
        target, clean, etc should store those as instance attributes.
        """
        pass

    @abstractmethod
    def setup(self, clean_t: torch.Tensor, *args, **kwargs) -> None:
        """
        Take in extra arguments and initialize targets or whatever is needed
        for this variant of the attack.
        All unknown arguments passed to `attack` will be passed to this method.
        """
        pass

    def mask(self, data):
        """
        Applied to delta after clamping. Does nothing here, but can be overridden.
        """
        return data

    def attack(
        self,
        clean: Image.Image,
		eps=16 / 255,
		alpha=2 / 255,
		steps=300,
		eot_samples: int = 1,
		eot_noise_std: float = 0.0,
		eot_brightness: float = 0.0,
		eot_shift_px: int = 0,
        *args, **kwargs
    ) -> Image.Image:
        clean_t = self._load_image_tensor(clean)
        clean_embed = self.model.embed_image(clean_t)

        self.setup(clean_t, *args, **kwargs)

        delta = torch.empty_like(clean_t).uniform_(-eps, eps)
        delta = (clean_t + delta).clamp(0, 1) - clean_t
        delta.requires_grad_(True)

        best_loss = float("inf")
        best_adv = clean_t.clone()

        for i in tqdm(range(steps), desc="AttackVLM"):
            adv = (clean_t + delta).clamp(0, 1)
            loss = 0.0
            n = max(1, int(eot_samples))

            for _ in range(n):
                adv_aug = self._eot_augment(adv, noise_std=eot_noise_std, brightness=eot_brightness, shift_px=eot_shift_px)
                adv_embed = self.model.embed_image(adv_aug)
                loss += self.compute_step_loss(adv_embed, clean_embed)

            loss = loss / n

            if loss.item() < best_loss:
                best_loss = loss.item()
                best_adv = adv.detach().clone()

            loss.backward()

            with torch.no_grad():
                delta.data = delta.data - alpha * delta.grad.sign()
                delta.data = delta.data.clamp(-eps, eps)
                delta.data = self.mask(delta.data)
                delta.data = (clean_t + delta.data).clamp(0, 1) - clean_t

            delta.grad.zero_()

        adv_np = best_adv[0].cpu().permute(1, 2, 0).numpy()
        adv_np = (adv_np * 255).round().clip(0, 255).astype(np.uint8)
        return Image.fromarray(adv_np)

    @staticmethod
    def _eot_augment(
        x: torch.Tensor,
		noise_std: float = 0.0,
		brightness: float = 0.0,
		shift_px: int = 0
    ) -> torch.Tensor:
        y = x
        if noise_std > 0:
            y = y + noise_std * torch.randn_like(y)
        if brightness > 0:
            scale = 1.0 + (2.0 * torch.rand((x.shape[0], 1, 1, 1), device=x.device) - 1.0) * brightness
            y = y * scale
        if shift_px > 0:
            dy = int(torch.randint(-shift_px, shift_px + 1, (1,), device=x.device).item())
            dx = int(torch.randint(-shift_px, shift_px + 1, (1,), device=x.device).item())
            y = torch.roll(y, shifts=(dy, dx), dims=(2, 3))
        return y.clamp(0, 1)

    def _load_image_tensor(self, image: Image.Image) -> torch.Tensor:
        """
        Load an image as a Tensor after converting to 512x512 float [0-1] format
        """
        image = image.convert("RGB").resize((512, 512))
        clean_np = np.array(image).astype(np.float32) / 255.0
        return torch.from_numpy(clean_np).permute(2, 0, 1).unsqueeze(0).to(self.device)


